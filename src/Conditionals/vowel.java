package Conditionals;

public class vowel {
    public static void main(String[] args) {
        char s = 'i';

        switch (s){
            case 'a':
                System.out.println("Vowel found");
                break;
            case 'e':
                System.out.println("Vowel found");
                break;
            case 'i':
                System.out.println("Vowel found");
                break;
            case 'o':
                System.out.println("Vowel found");
                break;
            case 'u':
                System.out.println("Vowel found");
                break;
            default:
                System.out.println("Vowel not found");
        }
    }
}
